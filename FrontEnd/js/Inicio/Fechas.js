document.addEventListener("DOMContentLoaded", function() {
  var ulElement = document.getElementById("nav-pills-list");
  var tabContentElement = document.getElementById("v-pills-tabContent");

  profiles.forEach(function (profile, index) {
    var liElement = document.createElement("li");
    liElement.className = "nav-item";
    var buttonElement = document.createElement("button");
    buttonElement.className = "nav-link";
    buttonElement.textContent = profile.name;
    buttonElement.setAttribute("data-bs-toggle", "pill");
    buttonElement.setAttribute("data-bs-target", "#profile-" + profile.id);
    buttonElement.setAttribute("type", "button");
    buttonElement.setAttribute("role", "tab");
    buttonElement.setAttribute("aria-controls", "profile-" + profile.id);
    buttonElement.setAttribute("aria-selected", index === 0 ? "true" : "false");
    liElement.appendChild(buttonElement);
    ulElement.appendChild(liElement);

    var tabPane = document.createElement("div");
    tabPane.className = "tab-pane fade";
    tabPane.id = "profile-" + profile.id;
    tabPane.setAttribute("role", "tabpanel");
    tabPane.setAttribute("aria-labelledby", "profile-" + profile.id + "-tab");

    var tableHTML =
      '<table class="table"><thead><tr>';
    profile.content.table.thead.forEach(function(column) {
      tableHTML += '<th class="columnastabla" scope="col">' + column.label + '</th>';
    });
    tableHTML += '</tr></thead><tbody class="table-group-divider">';
    profile.content.table.tbody.forEach(function(row) {
      tableHTML += '<tr>';
      row.rowData.forEach(function(cell) {
        tableHTML += '<td>' + cell + '</td>';
      });
      tableHTML += '</tr>';
    });
    tableHTML += '</tbody></table>';

    tabPane.innerHTML = tableHTML;
    tabContentElement.appendChild(tabPane);

    if (index === 0) {
      liElement.classList.add("active");
      tabPane.classList.add("show", "active");
    }
  });

  // Agregar evento de clic a todos los botones de perfil
  var profileButtons = document.querySelectorAll("#nav-pills-list .nav-link");
  profileButtons.forEach(function(button) {
    button.addEventListener("click", function() {
      var targetId = this.getAttribute("data-bs-target").substring(1);
      var tabPane = document.getElementById(targetId);
      
      // Ocultar todas las tablas
      var allTabPanes = document.querySelectorAll(".tab-pane.fade");
      allTabPanes.forEach(function(tab) {
        tab.classList.remove("show", "active");
      });

      // Mostrar la tabla correspondiente al perfil seleccionado
      tabPane.classList.add("show", "active");
    });
  });
});